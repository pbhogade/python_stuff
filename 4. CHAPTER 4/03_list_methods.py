# updates the list to ascending order 
l1 = [1, 8, 7, 2, 21, 15]
l1.sort()
print(l1)

l1.reverse() #this will updates the list in reverse order.
print(l1)

l1.append(8) #this will update the number 8 at the end.
print(l1)

l1.insert(3,5)#this will add number 5 at index 3.
print(l1)

l1.pop(2) #Will delete element at index 2 and return its value.
print(l1)

l1.remove(21) # Will remove 21 from the list.
print(l1)

